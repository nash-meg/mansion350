public class Registration {
    private String user;
    private String pass;

    public Registration(String user, String pass){
        this.user = user;
        this.pass = pass;
    }

    /*********** USERNAME ***********/
    public String getUser() {
        return user;
    }
    public void setUser(String user) {
        this.user = user;
    }

    /*********** PASSWORD ***********/
    public String getPass() {
        return pass;
    }
    public void setPass(String pass) {
        this.pass = pass;
    }





}


